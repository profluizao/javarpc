public class Calculadora {

    public int adicao(int x, int y) {
        return x + y;
    }

    public int subtracao(int x, int y) {
        return x - y;
    }
	
    public int multiplicacao(int x, int y) {
        return x * y;
    }
	
    public double divisao(int x, int y) {
        return x / y;
    }
}